// Data for matching 'apex2us' function
uint32_t apex2us_test[] = { 0x3D09000, 0x3BBA304, 0x3A728D2, 0x3931EF4, 0x37F8303, 0x36C52A2, 0x3598B85, 0x3472B6A, 0 };

// Special case for apex2us
int match_apex2us(firmware *fw, int k, uint32_t v1, uint32_t v2)
{
    if (isLDR_PC(fw,k) && (LDR2val(fw,k) == v1) && ((fwRd(fw,k) == 1) || (fwRd(fw,k) == 2)))
    {
        k = find_inst_rev(fw, isSTMFD_LR, k, 200);
        if (k != 0)
        {
            if (fwval(fw,k-2) == 0xE3700D09)    // CMN R0, #0x240
                k -= 2;
            uint32_t fadr = idx2adr(fw,k);
            fwAddMatch(fw,fadr,32,0,121);
            return 1;
        }
    }
    return 0;
}
int find_apex2us(firmware *fw, string_sig *sig, int j)
{
    int i;
    for (i=0; apex2us_test[i] != 0; i++)
        if (fwval(fw,j+i) != apex2us_test[i])
            return 0;

    return search_fw(fw, match_apex2us, idx2adr(fw,j), 0, 1);
}

// Special case for mkdir
int find_mkdir(firmware *fw, string_sig *sig, int k)
{
    if (fwval(fw,k) == 0x12CEA600)
    {
        k = find_inst_rev(fw, isSTMFD_LR, k-20, 200);
        if (k != 0)
        {
            if ((((fwval(fw,k+12) & 0xFFF0FFFF) == 0xE350002F) && ((fwval(fw,k+15) & 0xFFF0FFFF) == 0xE3500021) && ((fwval(fw,k+19) & 0xFFF0FFFF) == 0xE3500020)) ||
                (((fwval(fw,k+11) & 0xFFF0FFFF) == 0xE350002F) && ((fwval(fw,k+14) & 0xFFF0FFFF) == 0xE3500021) && ((fwval(fw,k+18) & 0xFFF0FFFF) == 0xE3500020)))
            {
                uint32_t fadr = 0;
                if (isBL(fw,k+47))
                {
                    fadr = followBranch(fw, idx2adr(fw,k+47), 0x01000001);
                }
                else if (isBL(fw,k+48))
                {
                    fadr = followBranch(fw, idx2adr(fw,k+48), 0x01000001);
                }
                if (fadr != 0)
                {
                    fwAddMatch(fw,fadr,32,0,121);
                    return 1;
                }
            }
        }
    }
    return 0;
}

// Special case for _pow
int find_pow(firmware *fw, string_sig *sig, int j)
{
    if (!idx_valid(fw,j) || !idx_valid(fw,j+3)) return 0;
    // Find values passed to _pow
    if ((fwval(fw,j) == 0x00000000) && (fwval(fw,j+1) == 0x40000000) && (fwval(fw,j+2) == 0x00000000) && (fwval(fw,j+3) == 0x408F4000))
    {
        uint32_t adr1 = idx2adr(fw,j);      // address of 1st value
        uint32_t adr2 = idx2adr(fw,j+2);    // address of 2nd value
        int j1;

        for (j1 = j-5; j1>0; j1--)
        {
            if (isADR_PC_cond(fw,j1) &&                 // ADR ?
                (fwval(fw,j1+1) == 0xE8900003) &&       // LDMIA R0,{R0,R1}
                isBL(fw,j1+2) &&                        // BL
                isADR_PC_cond(fw,j1+4))                 // ADR ?
            {
                if ((ADR2adr(fw,j1) == adr1) && (ADR2adr(fw,j1+4) == adr2))
                {
                    uint32_t fadr = followBranch(fw,idx2adr(fw,j1+2),0x01000001);
                    fwAddMatch(fw,fadr,32,0,121);
                    return 1;
                }
            }
            else
            if (isADR_PC_cond(fw,j1) &&                 // ADR ?
                (fwval(fw,j1+1) == 0xE8900003) &&       // LDMIA R0,{R0,R1}
                isBL(fw,j1+2) &&                        // BL
                isADR_PC_cond(fw,j1+3))                 // ADR ?
            {
                if ((ADR2adr(fw,j1) == adr1) && (ADR2adr(fw,j1+3) == adr2))
                {
                    uint32_t fadr = followBranch(fw,idx2adr(fw,j1+2),0x01000001);
                    fwAddMatch(fw,fadr,32,0,121);
                    return 1;
                }
            }
            else
            if (isADR_PC_cond(fw,j1) &&                 // ADR ?
                (fwval(fw,j1+2) == 0xE8900003) &&       // LDMIA R0,{R0,R1}
                isBL(fw,j1+3) &&                        // BL
                isADR_PC_cond(fw,j1+4))                 // ADR ?
            {
                if ((ADR2adr(fw,j1) == adr1) && (ADR2adr(fw,j1+4) == adr2))
                {
                    uint32_t fadr = followBranch(fw,idx2adr(fw,j1+3),0x01000001);
                    fwAddMatch(fw,fadr,32,0,121);
                    return 1;
                }
            }
        }
    }

    return 0;
}

